char	*ft_strrev(char *str)
{
	int i;
	int j;
	char temp;
	
	j = 0;
	i = 0;
	while (str [i] != '\0')
		i++;
	i--;
	while (j < i)
	{
		temp = str[i];
		str[i] = str[j];
		str[j] = temp;
		j++;
		i--;
	}
	if (i <= j)
		return(str);
	return (0);
}

