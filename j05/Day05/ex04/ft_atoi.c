int		ft_atoi(char *str)
{
	int		i;
	int		res;
	int		neg;

	i = 1;
	res = 0;
	neg = 1;
	while (str[i] && ((str[i] >= 8 && str[i] <= 14) || str[i] == 32))
		i++;
	if (str[i] == '-' || str[i] == '+')
	{
		if (str[i] == '-')
		{
			neg = -1;
			i++;
		}
		else
			i++;
	}
	while (str[i] && (str[i] >= '0' && str[i] <= '9'))
		res = res * 10 + (str[i++] - '0');
	return (res * neg);
}
